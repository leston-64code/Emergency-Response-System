const mongoose = require('mongoose'); // Erase if already required

var userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    mobile: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        enum: ["user", "admin"]
    }
},
    {
        timestamps: true
    }
);


module.exports = mongoose.model('User', userSchema);